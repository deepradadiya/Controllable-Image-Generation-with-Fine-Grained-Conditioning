            "2. After step 4: confirm condition maps exist "
            "(`ls data/conditions/{depth,pose,edge}/ | head`)\n"
            "3. After step 5: confirm weight files exist "
            "(`ls models/trained/*/model.safetensors`)\n"
            "4. After step 6: confirm evaluation results "
