"""
README Builder for Controllable Image Generation Project

Generates the final README.md content with architecture diagrams,
results tables, visual grids, reproducibility instructions, and links.

Uses a structured builder pattern with Python f-strings to produce
the complete README content.

Requirements Addressed: 11.1, 11.2, 11.3, 11.4, 14.1, 14.2, 14.3,
                        15.1, 15.2, 15.3, 15.4, 15.5, 16.1, 16.2, 16.3
"""

import sys
from pathlib import Path
from typing import Optional

# Add project root to path for imports
_project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(_project_root))

import importlib.util

_models_spec = importlib.util.spec_from_file_location(
    "src.app.models", _project_root / "src" / "app" / "models.py"
)
_models_module = importlib.util.module_from_spec(_models_spec)
_models_spec.loader.exec_module(_models_module)
PublishConfig = _models_module.PublishConfig


class ReadmeBuilder:
    """Builds the final README.md content for the GitHub repository.

    Generates a polished README with architecture diagrams, quantitative
    results tables, visual grids, reproducibility instructions, and links
    to all published artifacts.

    The build() method returns the full README content as a string.
    Sections are generated by private methods and assembled in order.

    Requirements Addressed: 11.1, 11.2, 11.3, 11.4, 14.1, 14.2, 14.3,
                            15.1, 15.2, 15.3, 15.4, 15.5, 16.1, 16.2, 16.3
    """

    def build(self, metrics: Optional[dict] = None, config: Optional[PublishConfig] = None) -> str:
        """Build the complete README.md content.

        Assembles all sections in the correct order:
        1. Header (title + description)
        2. Architecture diagrams
        3. Results table
        4. Visual grid
        5. Reproducibility
        6. Links
        7. Tech stack

        Args:
            metrics: Optional dictionary with evaluation metrics per
                condition type. Keys are condition types, values are
                dicts with 'fid_score' and 'alignment_score'.
            config: Optional PublishConfig for generating links and URLs.

        Returns:
            Complete README.md content as a string.
        """
        if metrics is None:
            metrics = {}
        if config is None:
            config = PublishConfig()

        sections = [
            self._header_section(),
            self._architecture_diagrams(),
            self._results_table(metrics),
            self._visual_grid_section(),
            self._reproducibility_section(),
            self._links_section(config),
            self._tech_stack_section(),
        ]

        return "\n".join(sections) + "\n"

    def _header_section(self) -> str:
        """Generate the README header with project title and description.

        The first line is a Markdown level-1 heading with the project title.
        The first non-empty line after the title is a plain-text description
        of at most 200 characters stating the project's capability and
        primary conditioning approach.

        Requirements Addressed: 16.1, 16.2, 16.3

        Returns:
            Header section string with title and description.
        """
        title = "# Controllable Image Generation with Fine-Grained Conditioning"
        description = (
            "Train and deploy ControlNet adapters for spatial conditioning "
            "(depth, pose, edge) on Stable Diffusion 1.5 with fine-grained control."
        )
        return f"{title}\n\n{description}"

