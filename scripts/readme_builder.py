            "└──────┬───────┘   │  (trainable)        │\n"
            "       |           └──────────┬──────────┘\n"
            "       |                      |\n"
            "       |           ┌──────────v──────────┐\n"
            "       |           │ Zero Convolution    │\n"
            "       |           └──────────┬──────────┘\n"
